/**
 * A size that has a width and a height.
 */
class Size2 {
  /**
   * Creates a new Size2.
   * @param {Number} width The width of the size.
   * @param {Number} height The height of the size.
   */
  constructor(width = 0, height = 0) {
    this.width = width;
    this.height = height;
  }
}

export default Size2;
